    private void transferSecondSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_transferSecondSubmitButtonActionPerformed
        // TODO add your handling code here:
        String receiverAccountNumber = receiverAccountNumberTextField.getText();
        String amount = transferAmountTextField.getText();
        String PIN = PINPasswordField.getText();

        long receiverAccountNum = Long.parseLong(receiverAccountNumber);
        double transferAmount = Double.parseDouble(amount);
        int pin = Integer.parseInt(PIN);

        if (pin == dbLoad.getDbPIN()) {
            if (dbLoad.depositBalanceforTransfer(transferAmount, receiverAccountNum) && dbLoad.withDrawBalance(transferAmount)) {
                JOptionPane.showMessageDialog(null, "Balance successfully Transferred to " + receiverAccountNum + " .");
            }
        } else {
            //custom title, error icon
            JOptionPane.showMessageDialog(null,
                    "Wrong PIN! Try using valid PIN.",
                    "wrong PIN",
                    JOptionPane.ERROR_MESSAGE);
            //JOptionPane.showMessageDialog(null, "Cannot Transfer at the moment. Please try again later.");
        }

        switchToPanel("menuPanelCode");
    }//GEN-LAST:event_transferSecondSubmitButtonActionPerformed

