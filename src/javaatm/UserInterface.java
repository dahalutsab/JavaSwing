    private void submitBtnPINChangeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitBtnPINChangeActionPerformed
        // TODO add your handling code here:
        char[] currentPINChars = currentPINField.getPassword();
        char[] newPINChars = newPINField.getPassword();
        char[] confirmPINChars = confirmPINField.getPassword();

        String currentStringPIN = String.valueOf(currentPINChars);
        String newStringPIN = String.valueOf(newPINChars);
        String confirmStringPIN = String.valueOf(confirmPINChars);

        if (currentStringPIN.matches("\\d+") && newStringPIN.matches("\\d+") && confirmStringPIN.matches("\\d+")) {
            int currentPIN = Integer.parseInt(currentStringPIN);
            int newPIN = Integer.parseInt(newStringPIN);
            int confirmPIN = Integer.parseInt(confirmStringPIN);
            int dbPIN = dbLoad.getDbPIN();
            
            if (dbPIN == currentPIN) {
                if ( newPIN == confirmPIN) {
                    dbLoad.setPIN(confirmPIN);
                    JOptionPane.showMessageDialog(null, "PIN successfully Changed.");
                    currentPINField.setText("");
                    newPINField.setText("");
                    confirmPINField.setText("");
                    switchToPanel("menuPanelCode");
                } else {
                     JOptionPane.showMessageDialog(null, "New PIN and Confirm PIN doesn't match!");
                     newPINField.setText("");
                     confirmPINField.setText("");
                }
            }else {
                JOptionPane.showMessageDialog(null, "Old PIN doesn't match.");
                currentPINField.setText("");
                newPINField.setText("");
                confirmPINField.setText("");
            }
        }else {
            wrongDepositChoice.setText("Please input the amount in Digits only!");
            //return;
        }
    }//GEN-LAST:event_submitBtnPINChangeActionPerformed

